char NoteKey(int vk)
{
	switch (g_keyboard_layout)
	{
	case KEYBOARD_QWERTY:
		return keynotes_QWERTY[vk];

	case KEYBOARD_AZERTY:
		return keynotes_AZERTY[vk];

	default:
		return -1;	// return INVALID;
	}
}

char NumbKey(int vk)
{
	return keynumbs[vk];
}

char Numblock09Key(int vk)
{
	return keynumblock09[vk];
}
